# starts from 0
for i in range(10):
    print(i, end = " ")

print("\n**********************************************************")

# excludes upper limit
for i in range(1,8):
    print(i, end = " ")

print("\n**********************************************************")

# else in for loop
item = [1, 2, 3, 4, 5]
for i in item:
    print(i, end = " ")
else:
    print('\nNo numbers left')

print("**********************************************************")